// Crie um sistema simples de menu de opções.

let opcao = 3;

switch (opcao) {
  case 1:
    console.log("Cadastrar usuário");
    break;
  case 2:
    console.log("Listar usuários");
    break;
  case 3:
    console.log("Excluir usuário");
    break;
  default:
    console.log("Opção inválida");
}